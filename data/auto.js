require('dotenv-safe').config({
  path: '../.env',
  sample: '../.env.example'
});
var SequelizeAuto = require('sequelize-auto');

var auto = new SequelizeAuto(process.env.DB_DATABASE, process.env.DB_USERNAME, process.env.DB_PASSWORD, {
    host: process.env.DB_HOST,
    dialect: process.env.DB_DIALECT,
    directory: `${__dirname}/models/autogenerated`,
    additional: {
        timestamps: false,
        freezeTableName: true
    },
    tables: [
      'users',
      'userPhotos',
      'userTimesheets',
      'userSuitabilityTypes',
      'suitabilityTypes',
      'clients',
      'clientFavourites',
      'events',
      'eventShifts',
      'EventShiftSuitabilityTypes',
      'venues',
			'dressCodes',
			'jobRoles',
    ]
});

auto.run(function (err) {
  if (err) throw err;
  console.log(auto.tables);
  console.log(auto.foreignKeys);
});
